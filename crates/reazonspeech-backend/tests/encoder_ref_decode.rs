//! Debug: feed the Python-reference encoder output through the Rust
//! RNN-T decoder + tokenizer. If Japanese text comes out, the Python
//! reference encoder in `scripts/dump-encoder-fixtures.py` is correct
//! and any drift is in the Rust encoder. If the output is gibberish,
//! both implementations are diverging from NeMo and the Python ref
//! itself needs fixing first.
//!
//! Skipped when the prerequisites aren't present:
//!   - `/tmp/reazon.gguf` (converted from .nemo)
//!   - `/tmp/reazon.tokenizer.model` (companion SP model)
//!   - `crates/fastconformer-core/tests/fixtures/reazonspeech-nemo-v2/encoder/encoder_output.npy`
//!     (generated by `scripts/dump-encoder-fixtures.py`)

use std::fs;
use std::path::{Path, PathBuf};

use fastconformer_core::encoder::EncoderOutput;
use fastconformer_core::{log_mel_spectrogram, Config, SentencePieceTokenizer};
use gguf_loader::GgufFile;
use reazonspeech_backend::{decoder::RnntDecoder, encoder};

fn read_npy_f32_2d(path: &Path) -> Option<(Vec<f32>, [usize; 2])> {
    let bytes = fs::read(path).ok()?;
    if &bytes[..6] != b"\x93NUMPY" {
        return None;
    }
    let major = bytes[6];
    let (header_len, header_start) = match major {
        1 => (
            u16::from_le_bytes([bytes[8], bytes[9]]) as usize,
            10_usize,
        ),
        2 | 3 => (
            u32::from_le_bytes([bytes[8], bytes[9], bytes[10], bytes[11]]) as usize,
            12_usize,
        ),
        _ => return None,
    };
    let header = std::str::from_utf8(&bytes[header_start..header_start + header_len]).ok()?;
    if !header.contains("'<f4'") {
        return None;
    }
    let open = header.find('(')?;
    let close = header[open..].find(')')? + open;
    let dims: Vec<usize> = header[open + 1..close]
        .split(',')
        .filter_map(|s| s.trim().parse::<usize>().ok())
        .collect();
    if dims.len() != 2 {
        return None;
    }
    let body = &bytes[header_start + header_len..];
    let n = dims[0] * dims[1];
    let mut out = Vec::with_capacity(n);
    for i in 0..n {
        let b = &body[i * 4..i * 4 + 4];
        out.push(f32::from_le_bytes([b[0], b[1], b[2], b[3]]));
    }
    Some((out, [dims[0], dims[1]]))
}

#[test]
fn python_ref_encoder_output_decodes_to_japanese_text() {
    let gguf_path = PathBuf::from("/tmp/reazon.gguf");
    let tok_path = PathBuf::from("/tmp/reazon.tokenizer.model");
    // Prefer NeMo ground-truth dump (from scripts/nemo-truth-dump.py).
    // Fall back to the hand-rolled reference if not present.
    let truth = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("../fastconformer-core/tests/fixtures/reazonspeech-nemo-v2/encoder_truth/encoder_output.npy");
    let hand = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("../fastconformer-core/tests/fixtures/reazonspeech-nemo-v2/encoder/encoder_output.npy");
    let enc_out_path = if truth.exists() { truth } else { hand };
    eprintln!("using encoder output: {}", enc_out_path.display());

    if !gguf_path.exists() || !tok_path.exists() || !enc_out_path.exists() {
        eprintln!("SKIPPED: missing prerequisites:");
        eprintln!("  gguf:    {} (exists={})", gguf_path.display(), gguf_path.exists());
        eprintln!("  tokens:  {} (exists={})", tok_path.display(), tok_path.exists());
        eprintln!("  enc_out: {} (exists={})", enc_out_path.display(), enc_out_path.exists());
        return;
    }

    let gguf = GgufFile::open(&gguf_path).expect("open gguf");
    let cfg = Config::from_gguf(&gguf).expect("parse config");
    let decoder = RnntDecoder::from_gguf(&gguf, cfg.clone()).expect("load decoder");
    let tokenizer = SentencePieceTokenizer::load(&tok_path).expect("load tokenizer");

    let (data, shape) = read_npy_f32_2d(&enc_out_path).expect("read encoder_output.npy");
    eprintln!("loaded ref encoder output shape: [{}, {}]", shape[0], shape[1]);
    assert_eq!(shape[1], cfg.d_model as usize, "d_model mismatch");

    let enc_out = EncoderOutput {
        data,
        n_frames: shape[0],
        d_model: shape[1],
    };

    let token_ids = decoder.greedy_decode(&enc_out);
    eprintln!("decoded {} token ids", token_ids.len());
    eprintln!("token ids (first 50): {:?}", &token_ids[..token_ids.len().min(50)]);

    let text = tokenizer.detokenize(&token_ids).expect("detokenize");
    eprintln!("text: {text:?}");

    // We don't assert specific content yet — just want to see what comes out.
    // For Japanese audio the expected text starts with "嫌だ" and continues
    // with conversation.
    assert!(
        !text.is_empty(),
        "no text decoded — Python ref encoder may be broken"
    );
}

